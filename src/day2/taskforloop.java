package day2;

public class taskforloop {

	public static void main(String[] args) {
		
		for (int j=1;j<=10;j++)
		{ 
			int i=2;
			int k=i*j; 
		System.out.println(i+"*"+j+"="+k);
		}
	System.out.println("=============");

	for (int j=1;j<=10;j++)
	{ 
		int i=3;
		int k=i*j; 
	System.out.println(i+"*"+j+"="+k);
	}
	}
	

}
