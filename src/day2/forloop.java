package day2;

public class forloop {

	public static void main(String[] args) {
			for (int k=1;k<=5;k++)
			{
			System.out.println("sharief ");
			}
			for (int i=5;i>=1;i--)
			{
			System.out.println("rajesh "+i);
			}
			for (int i=15;i>=1;i-=2)
			{
			System.out.println("raja "+i);
			}
	}
	
	

	}


